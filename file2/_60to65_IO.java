package file2;

public class _60to65_IO {
    public static void main(String[] args) {
        System.out.println(args.length);

        /*
        input output IO->입출력

        String[] args{}
        문자열을 담을 수 있는 배열이다.
        args 라는 이름을 가지고 있는 변수를 정의할것이며
        그 변수는 문자열의 데이터 타입을 담을 수 있는 배열이다

        main{}사이에 속해있으므로
        메인이라는 이름의 메소드에 파라미터값으로 속해있음

        void 메인이라는 메소드는 출력값이 존재하지 않는다는 의미

        args.length
        args라는 배열에 담겨있는

         */
    }
}
